**                 Comparison Type Opcodes              **
*********************************************************/
`define SEQ 5'b11100
`define SLT 5'b11101
`define SLE 5'b11110
`define SCO 5'b11111

/*********************************************************
**                     Memory Opcodes                   **
*********************************************************/
