**                  I-Type Logical Opcodes              **
*********************************************************/
`define ADDi   5'b01000
`define SUBi   5'b01001
`define XORi   5'b01010
`define ANDNi  5'b01011

/*********************************************************
**                   I-Type Shift Opcodes               **
*********************************************************/
