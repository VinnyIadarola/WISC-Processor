`define NOP     5'b00001
`define SIIC_ILLEGAL_OP 5'b00010
`define SIIC_PC_EPC 5'b00011

/*********************************************************
**                 Function Code Opcodes                **
*********************************************************/
`define LOGICAL_R_INSTR 5'b11011
`define SHIFT_R_INSTR 5'b11010

